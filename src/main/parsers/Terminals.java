	static public final short AND = 12;
	static public final short DATA = 13;
	static public final short IF = 14;
	static public final short READ = 15;
	static public final short ITERATE = 16;
	static public final short PRINT = 17;
	static public final short RETURN = 18;
	static public final short CLOSEPARENTHESIS = 19;
